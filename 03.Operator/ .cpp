#include <iostream>

using namespace std;

int main() {
    int num1 = 40;
    int num2 = 20;
    
    cout << "num1 + num2 = " << num1 + num2 << endl ;
    cout << "num1 - num2 = " << num1 - num2 << endl ;
    cout << "num1 * num2 = " << num1 * num2 << endl ;
    cout << "num1 / num2 = " << num1 / num2 << endl ;
    
    int output = (num1 + num2) * 10 ;
    cout << output << endl;
}
